    public function store(Request $request)
    {
        $plan = new Plan();
        $plan->nomb_plan = $request->nomb_plan;
        $plan->precio = $request->precio;
        $plan->cant_dispositivos = $request->cantidad;
        $plan->save();

        return back();
    }

